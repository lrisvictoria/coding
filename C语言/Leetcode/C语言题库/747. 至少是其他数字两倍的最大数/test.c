//给你一个整数数组 nums ，其中总是存在 唯一的 一个最大整数 。

//请你找出数组中的最大元素并检查它是否 至少是数组中每个其他数字的两倍 。如果是，则返回 最大元素的下标 ，否则返回 - 1 。
//
//示例 1：
//
//输入：nums = [3, 6, 1, 0]
//输出：1
//解释：6 是最大的整数，对于数组中的其他整数，6 至少是数组中其他元素的两倍。6 的下标是 1 ，所以返回 1 。
//示例 2：
//
//输入：nums = [1, 2, 3, 4]
//输出： - 1
//解释：4 没有超过 3 的两倍大，所以返回 - 1 。
//示例 3：
//
//输入：nums = [1]
//输出：0
//解释：因为不存在其他数字，所以认为现有数字 1 至少是其他数字的两倍。

int dominantIndex(int* nums, int numsSize)
{
	int first_max = -1;//最大值
	int second_max = -1;//次大值
	int ans = -1;//返回下标
	for (int i = 0; i < numsSize; i++)
	{
		if (nums[i] > first_max)
		{
			second_max = first_max;//记录上一次first_max的值
			first_max = nums[i];//最大值等于这次的元素
			ans = i;//记录下标
		}
		else if (nums[i] > second_max)//两个分支只能进行一次，要么记录最大值，要么记录次大值，要么不记录
		{
			second_max = nums[i];
		}
	}
	return (first_max >= 2 * second_max) ? ans : -1;//最大值至少为次大值的两倍才能返回下标，否则返回-1
}